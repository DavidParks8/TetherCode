import { createHash } from 'node:crypto';
import { execFileSync } from 'node:child_process';
import { readFile, writeFile } from 'node:fs/promises';
import { createRequire } from 'node:module';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

import { EventSchemas } from '@ag-ui/core';
import { quicktype, InputData, JSONSchemaInput, FetchingJSONSchemaStore } from 'quicktype-core';
import { zodToJsonSchema } from 'zod-to-json-schema';

const require = createRequire(import.meta.url);
const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const packageJsonPath = require.resolve('@ag-ui/core/package.json');
const packageJson = JSON.parse(await readFile(packageJsonPath, 'utf8'));
const packageEntryPath = require.resolve('@ag-ui/core');
const sourceHash = createHash('sha256')
  .update(await readFile(packageEntryPath))
  .digest('hex');
const schemaPath = path.join(root, 'contracts/bridge-rpc/v2/ag-ui-event.schema.json');
const rustPath = path.join(root, 'services/rust-bridge/src/agui_generated.rs');

const generatedSchema = zodToJsonSchema(EventSchemas, {
  name: 'AgUiEvent',
  target: 'jsonSchema7',
});
const schemaArtifact = {
  $comment: `Generated from @ag-ui/core@${packageJson.version} EventSchemas; source sha256 ${sourceHash}`,
  'x-ag-ui-core-version': packageJson.version,
  'x-ag-ui-core-source-sha256': sourceHash,
  ...generatedSchema,
};
const schemaText = `${JSON.stringify(schemaArtifact, null, 2)}\n`;

const schemaInput = new JSONSchemaInput(new FetchingJSONSchemaStore());
await schemaInput.addSource({ name: 'AgUiEvent', schema: schemaText });
const inputData = new InputData();
inputData.addInput(schemaInput);
const rust = await quicktype({
  inputData,
  lang: 'rust',
  rendererOptions: {
    visibility: 'crate',
    density: 'normal',
    'derive-debug': 'true',
    'skip-serializing-none': 'true',
  },
});
const unformattedRust = [
  `// @generated by scripts/generate-agui-bindings.mjs from @ag-ui/core@${packageJson.version}.`,
  `// Source sha256: ${sourceHash}. Do not edit by hand.`,
  ...rust.lines,
  '',
].join('\n');
const rustText = execFileSync('rustfmt', ['--emit', 'stdout'], {
  input: unformattedRust,
  encoding: 'utf8',
});

const check = process.argv.includes('--check');
for (const [target, expected] of [[schemaPath, schemaText], [rustPath, rustText]]) {
  if (check) {
    const actual = await readFile(target, 'utf8').catch(() => '');
    if (actual !== expected) {
      throw new Error(`${path.relative(root, target)} is stale; run npm run agui:generate`);
    }
  } else {
    await writeFile(target, expected);
  }
}

console.log(`AG-UI bindings ${check ? 'match' : 'generated from'} @ag-ui/core@${packageJson.version} (${sourceHash.slice(0, 12)}).`);
